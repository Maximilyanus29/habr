    <h2 id="culture">Хабраэтикет</h2>
    <p>Это не&nbsp;рекомендации для обязательного выполнения, это&nbsp;— <a href="https://ru.wikipedia.org/wiki/%D0%A1%D0%B5%D1%82%D0%B5%D0%B2%D0%BE%D0%B9_%D1%8D%D1%82%D0%B8%D0%BA%D0%B5%D1%82">правила этикета</a>, придерживаться которых совсем несложно.</p>

    <ul>
      <li>Сетевой этикет&nbsp;— он&nbsp;и&nbsp;на&nbsp;Хабре сетевой этикет.</li>
      <li>Уважайте мнение других. Оно не&nbsp;обязано совпадать с&nbsp;вашим.</li>
      <li>Откажитесь от&nbsp;постинга сообщений, направленных лишь на&nbsp;увеличение Хабрасилы. Стремитесь просто писать интереснее и&nbsp;лучше.</li>
      <li>Аргументированная публикация, даже если она&nbsp;противоположна вашему мнению, не&nbsp;должна получать минус. Человек не&nbsp;обязан подстраиваться под ваши вкусы.</li>
      <li>Если схожие темы неоднократно поднимаются под разными предлогами, это не&nbsp;значит, что надо ставить минус. Это лишь значит, что проблема до&nbsp;сих пор не&nbsp;решена.</li>
      <li>Каждый автор имеет свою индивидуальность, потому если вы&nbsp;уже где-то видели подобную статью, это не&nbsp;значит, что в&nbsp;сообщении нет ничего нового и&nbsp;интересного. Вчитайтесь и&nbsp;вдумайтесь&nbsp;— поймите авторское мнение.</li>
      <li>Изменения кармы несёт гораздо большую ответственность, чем выставление оценки комментарию. <nobr>1-2</nobr> комментария не&nbsp;должны становиться причиной для занижения кармы.</li>
      <li>Не&nbsp;злоупотребляйте своей возможностью голосования. Необходимо понимать, что минус сильно отличается от&nbsp;плюса&nbsp;— минус угнетает человека, а&nbsp;не&nbsp;развивает его. Ставьте плюсы, когда вам что-то нравится, но&nbsp;подумайте, прежде чем ставить минус, если что-то не&nbsp;понравилось.</li>
      <li>Первый комментарий&nbsp;— это не&nbsp;единственный комментарий в&nbsp;теме. Прежде чем ставить ему оценку, прочитайте ещё парочку.</li>
      <li>Минус&nbsp;— это не&nbsp;аргумент, и, тем более, не&nbsp;контраргумент.</li>
      <li>Не&nbsp;стоит делать того, что не&nbsp;принято в&nbsp;цивилизованном обществе, противоречит законам, морали и&nbsp;этике.</li>
      <li>Не&nbsp;делай другим&nbsp;то, что не&nbsp;хочешь получить от&nbsp;них сам. Поставьте себя на&nbsp;место человека, с&nbsp;которым общаетесь. Отстаивайте свою точку зрения, но&nbsp;не&nbsp;оскорбляйте окружающих.</li>
      <li>Помогайте другим там, где вы&nbsp;это можете делать.</li>
    </ul>

