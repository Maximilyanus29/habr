      <li>Я&nbsp;не&nbsp;создаю глупых комментариев с&nbsp;содержанием «+1», «ф&nbsp;топку», «афтар жжот».</li>
      <li>Я&nbsp;отказываюсь от&nbsp;употребления матерных слов и&nbsp;оскорблений на&nbsp;страницах ресурса.</li>
      <li>Я&nbsp;прикладываю максимум усилий для того, чтобы мои тексты были написаны без ошибок.</li>
