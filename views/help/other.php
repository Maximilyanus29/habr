    <h2 id="habraeffect">Хабраэффект</h2>
    <p><b>Хабраэффект</b>&nbsp;— резкое увеличение посещаемости сайта после того, как ссылка на&nbsp;данный ресурс появилась на&nbsp;Хабре.</p>

    <p>Бяка данного эффекта заключается в&nbsp;том, что из-за резкого повышения посещаемости сайта генерируется огромный трафик, превращаясь в&nbsp;неумышленную, но&nbsp;тем не&nbsp;менее небезобидную DoS-атаку.</p>

    <p>Хабраэффект чаще всего наблюдается в&nbsp;течение нескольких десятков минут, но&nbsp;может затянуться и&nbsp;до&nbsp;нескольких часов. Наплыв пользователей может не&nbsp;проходить и&nbsp;в&nbsp;течение дня&nbsp;— двух, пока новость с&nbsp;адресом ресурса не&nbsp;пропадёт с&nbsp;главной страницы Хабра.</p>

    <p>Бытует мнение, что хабраэффекту подвержены только новые и&nbsp;не&nbsp;раскрученные сайты с&nbsp;малыми мощностями, но&nbsp;это не&nbsp;так. В&nbsp;своё время хабраэффект смог «положить на&nbsp;лопатки» немало крупных проектов&nbsp;— обо всех случаях можно узнать из&nbsp;<a title="Великий и ужасный Хабраэффект )" href="/search/?q=хабраэффект">несложного поискового запроса</a> на&nbsp;Хабре.</p>

