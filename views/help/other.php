      <li>В&nbsp;случае необходимости я&nbsp;помогаю новичкам на&nbsp;ресурсе.</li>
      <li>Я&nbsp;пользуюсь поиском для уточнения, был&nbsp;ли подготовленный мной материал опубликован на&nbsp;ресурсе ранее. Если это произошло, я&nbsp;дополняю его в&nbsp;комментариях.</li>
      <li>Каждым своим действием на&nbsp;ресурсе я&nbsp;стремлюсь добавить порядка, а&nbsp;не&nbsp;внести хаоса.</li>
