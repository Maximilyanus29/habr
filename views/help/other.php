    <h2 id="hotkeys">Горячие клавиши</h2>
    <p>Для тех, кто привык осуществлять навигацию по&nbsp;сайтам с&nbsp;помощью клавиатуры, мы&nbsp;прописали ряд сочетаний. Если они вам мешают, вы&nbsp;можете отключить их&nbsp;в настройках профиля.</p>

    <p>Страницы со&nbsp;списком публикаций поиска и&nbsp;так далее</p>

    <ul>
      <li><code>j</code>&nbsp;— переход к&nbsp;следующему элементу списка</li>
      <li><code>k</code>&nbsp;— переход к&nbsp;предыдущему элементу списка</li>
      <li><code>h</code>&nbsp;— прокрутка в&nbsp;самый верх страницы</li>
      <li><code>l</code>&nbsp;— прокрутка в&nbsp;самый низ страницы</li>
      <li><code>o</code>&nbsp;— открыть выбранную публикацию</li>
    </ul>

    <p>Страница публикации:</p>

    <ul>
      <li><code>c</code>&nbsp;— написание нового комментария: прокрутка к&nbsp;форме написания комментария с&nbsp;установкой фокуса в&nbsp;неё</li>
      <li><code>r</code>&nbsp;— обновление/подгрузка комментариев</li>
      <li><code>t</code>&nbsp;— отслеживать новые комментарии в&nbsp;трекере</li>
      <li><code>m</code>&nbsp;— присылать уведомления о&nbsp;новых комментариях на&nbsp;почту</li>
      <li><code>f</code>&nbsp;— переход к&nbsp;следующему непрочитанному комментарию (с пометкой комментария как прочитанный)</li>
      <li><code>k</code>&nbsp;— переход к предыдущему непрочитанному комментарию</li>
      <li><code>j</code>&nbsp;— переход к следующему непрочитанному комментарию</li>
    </ul>

    <p>Вообще все страницы</p>

    <ul>
      <li><code>CTRL/ALT</code> + <code>Enter</code>&nbsp;— отправка данных из&nbsp;формы, в&nbsp;которой находится курсор (поле комментариев, поиск и&nbsp;так далее)</li>
      <li><code>/</code> — прокрутка к&nbsp;полю поиска с&nbsp;установкой фокуса на&nbsp;него</li>
      <li><code>ESC</code> — закрытие левой навигационной панели</li>
    </ul>

    <p>А&nbsp;ниже просто идёт всякая всячина, которую кто-то когда-то придумал.</p>

