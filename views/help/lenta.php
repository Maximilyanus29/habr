    <h3 id="allowed_tags_formulae">Добавление формул</h3>
    <p>Вы&nbsp;также можете добавлять в&nbsp;свои публикации математические формулы, используя язык разметки <a href="https://ru.wikipedia.org/wiki/LaTeX">LaTeX</a>.
    </p><p>Для отрисовки формул на&nbsp;странице мы&nbsp;используем библиотеку <a href="https://www.mathjax.org/">MathJax</a>.</p>
    Чтобы добавить формулу нажмите на&nbsp;иконку&nbsp;<code>Σ</code> в&nbsp;тулбаре. В&nbsp;появившемся модальном окне выберите строчный или блочный тип формулы.<p></p>
    <ul>
      <li>cтрочная формула используется для вставки формулы в&nbsp;абзац текста</li>
      <li>блочная формула используется для вставки формулы с&nbsp;новой строки</li>
    </ul>
    <p>После составления формулы нажмите на&nbsp;кнопку «Добавить формулу» и&nbsp;она появится в&nbsp;тексте публикации.</p>

    <h2 id="ppa">Программа поощрения авторов</h2>
    <p>Аббревиатура «ППА» на&nbsp;Хабре расшифровывается как Программа поощрения авторов. Помимо признания аудитории, авторы могут получать денежное вознаграждение за&nbsp;хорошие публикации&nbsp;— очевидно&nbsp;же!
    В&nbsp;программе могут принимать участие пользователи, обладающие хотя&nbsp;бы одним из&nbsp;<a title="Значки" href="/info/help/habracentre/#badgets">значков</a>: «Автор», «Звезда», «Легенда» и&nbsp;«Старожил». А&nbsp;дальше, всё, что нужно&nbsp;— просто размещать интересные публикации или переводы в&nbsp;<a title="Хабы" href="/info/help/hubs/#hubs">профильные хабы</a>, не&nbsp;более того. Более подробно узнать о&nbsp;ППА можно на&nbsp;<a title="ППА" href="/ppa/faq/">отдельной странице</a>.</p>

    <h2 id="limits">Ограничения на&nbsp;количество публикаций</h2>
    <p><a title="Полноправный аккаунт" href="/info/help/registration/#standard">Полноправные</a> пользователи могут без предварительной модерации размещать на&nbsp;сайте любые публикации, соответствующие <a title="Хабы" href="/info/help/hubs/">тематике</a> ресурса и&nbsp;не&nbsp;нарушающие <a title="Правила сайта" href="/info/help/rules">правил</a> сообщества, однако существует несколько ограничений:</p>
    <ul>
