    <p>Самое важное, что есть на&nbsp;сайте&nbsp;— это публикации. Создавать их&nbsp;могут пользователи с&nbsp;полноправными аккаунтами и&nbsp;положительной кармой. При этом для некоторых хабов входной порог по&nbsp;карме может отличаться.</p>

    <h2 id="create-post">Создание поста</h2>

    <p>Процесс создания публикации прост:</p>
    <ul>
      <li>нажмите на&nbsp;кнопку «Написать» справа в&nbsp;шапке сайта;</li>
      <li>выберите тип поста: статью, новость или перевод;</li>
      <li>выберите от&nbsp;одного до&nbsp;пяти хабов, которые подходят по&nbsp;тематике;</li>
      <li>если публикация&nbsp;— это урок, how-to или tutorial&nbsp;— отметьте чекбокс «Обучающий материал». Это поможет визуально выделить ее&nbsp;среди прочих;</li>
      <li>дайте публикации понятный заголовок, заполните тело публикации, используя HTML- или markdown-разметку для форматирования текста;</li>
      <li>не&nbsp;забудьте указать теги, по&nbsp;которым можно понять, о&nbsp;чем пост;</li>
      <li>при желании&nbsp;— добавьте опрос с&nbsp;вариантами ответа;</li>
      <li>c&nbsp;помощью кнопки «Предпросмотр» оцените&nbsp;то, что у&nbsp;вас получилось (возможно, где-то не&nbsp;закрыт тег, не&nbsp;вставилась картинка или еще что);</li>
      <li>если всё в&nbsp;порядке, нажмите зеленую кнопку «Опубликовать»;</li>
      <li>если в&nbsp;пост нужно внести изменения, воспользуйтесь кнопкой в&nbsp;виде карандаша справа от&nbsp;заголовка&nbsp;— это позволит отредактировать материал или убрать в&nbsp;черновики.</li>
    </ul>
    <p>Черновики видны только вам (о&nbsp;статусе черновика говорит иконка замка слева от&nbsp;заголовка публикации).</p>

    <h2 id="create-post-article">Статья</h2>
    <p>Под статьей на&nbsp;Хабре понимают оригинальный материал, написанный лично автором. Обзор, дайджест, туториал, ликбез, отчет о&nbsp;мероприятии, рецензия&nbsp;— все это статьи.</p>

    <h2 id="create-post-translate">Перевод</h2>
    <p>Если вы&nbsp;хотите разместить перевод чей-то статьи, не&nbsp;забудьте изменить тип поста на&nbsp;«Перевод». Механизм создания тот&nbsp;же, что и&nbsp;у&nbsp;статьи, но&nbsp;появятся два дополнительных поля: «Автор оригинала» (тут надо указать имя автора оригинального текста) и&nbsp;«Ссылка на&nbsp;оригинал» (здесь&nbsp;— URL страницы оригинала).</p>

    <h2 id="create-post-news">Новость</h2>
    <p></p>Новости&nbsp;— информация, которую ценят за&nbsp;свежесть. Это емкие сообщения о&nbsp;неизвестных событиях, которые произошли недавно или происходят прямо сейчас. В&nbsp;новостном посте не&nbsp;место личному мнению&nbsp;— факты и&nbsp;только факты. Новость должна ответить на&nbsp;шесть главных вопросов: кто, что, где, когда, почему и&nbsp;как. Из&nbsp;первого абзаца читатель точно должен узнать ответы на&nbsp;первые четыре вопроса. Почему и&nbsp;как все произошло&nbsp;— дальше.<p></p>

    <h2 id="black-and-white">Признаки хороших и&nbsp;плохих публикаций</h2>
    <p><strong>Хорошая публикация:</strong></p>
    <ul>
      <li>содержит авторский контент;</li>
      <li>удобна для чтения, разбита на&nbsp;абзацы и&nbsp;в&nbsp;целом хорошо отформатирована, а&nbsp;картинки залиты на&nbsp;<a href="https://habrastorage.org">Habrastorage</a>;</li>
      <li>написана максимально грамотно;</li>
      <li>не&nbsp;содержит того, что есть в&nbsp;плохих публикациях.</li>
    </ul>

    <p><strong>Плохая публикация:</strong></p>
    <ul>
      <li>имеет кричащий заголовок вида «Шок! ВИДЕО! ВИРУСЫ В&nbsp;МАКАХ!»;</li>
      <li>переполнена грамматическими и&nbsp;орфографическими ошибками, игнорирует правила русского языка в&nbsp;целом;</li>
      <li>содержит слова из&nbsp;«падонкаффского» сленга и&nbsp;прочую нечисть;</li>
      <li>содержит изображения с&nbsp;нестабильных фотохостингов;</li>
      <li>является жалобой на&nbsp;что-либо;</li>
      <li>содержит реферальные ссылки на&nbsp;что угодно;</li>
      <li>содержит запрещённый или просто неуместный контент;</li>
      <li>является прямой рекламой своего или чужого проекта, сервиса, приложения, сайта и&nbsp;т.&nbsp;п.</li>
    </ul>

